// Math: proporciona métodos contendo operações matemáticas.

let num1 = 9.89123;

num1 = Math.floor(num1); //arredondar para baixo.
console.log(num1); //

num1 = Math.ceil(num1); //arredondar para cima.
console.log(num1); //

num1 = Math.round(num1); //arredondar para o mais próximo.
console.log(num1); //

console.log(Math.max(1,6,8,9,5,2,6,3)); //retorna o maior n.

console.log(Math.min(1,6,8,9,5,2,6,3)); //retorna o menor n.

console.log(Math.round(Math.random() * (10 - 5) + 5)); //retorna n. aleatório entre 5 e 10.

console.log(Math.PI); 

console.log(Math.trunc(num1));

console.log(Math.pow(2, 10));




