/**
 * > - maior que.
 * >= - maior que ou igual a. 
 * < - menor que.
 * <= - menor que ou igual a.
 * == - igual a (checa valor) ***** Não usar.
 * === - igual estritamente a (checa valor e tipo).
 * != - diferente que (checa valor) ***** Não usar.
 * !== - estritamente diferente que (checa valor e tipo).
 */

console.log(10 > 5); // retorna true.
console.log(10 >= 10); // retorna true.
console.log(10 < 5); // retorna false.
console.log(10 <= 5); // retorna false.
console.log(10 == '10'); // retorna true. A linguagem corrige o tipo.
console.log(10 != '10'); // retorna false.
console.log(10 === '10'); // retorna false. Tipos diferentes.
console.log(10 !== '10'); // retorna true. Tipos diferentes.
